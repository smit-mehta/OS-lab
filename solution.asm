; Calculation part
; ----------------

; for (i = 0; i < 10; i ++) {
;   for (j = 0; j < 30; j ++) {
;     C[i][j] = 0;
;     for (k = 0; k < 20; k ++) 
;       C[i][j] += A[i][k].B[k][j]; 
;   }
; }

; esi register maintains the value of i
; edi register maintains the offset of the element in C that is 
;   currently computed.
mov esi, 0
mov edi, 0

; Outer most loop - 'i' loop
L1:
  cmp esi,10                ; Compare i and m = 10
  je near end1              ; Equal => break loop1

  ; ebx maintains the value of j
  mov ebx, 0

  ; middle loop - 'j' loop
  L2:
    cmp ebx, 30             ; Compare j and p = 30
    je end2                 ; Equal => break loop2

    ; set C[i][j] = 0
    mov dword [fs:edi], 0
    mov dword [fs:edi + 4], 0

    ; ecx maintains the value of k
    mov ecx, 0

    ; inner most loop - 'k' loop
    L3:
      cmp ecx, 20            ; Compare k and n = 20
      je end3                ; Equal => break loop3
      
      ; Compute the offset of A[i][k] in the register eax
      ; eax = (20*i + k) * 4 bytes
      mov eax, esi
      imul eax, 20
      add eax, ecx
      shl eax, 2
      ; push A[i][k] into the stack
      push dword [ds:eax]
      
      ; Compute the offset of B[k][j] in the register eax
      ; eax = (30*k + j) * 4 bytes
      mov eax, ecx
      imul eax, 30
      add eax, ebx
      shl eax, 2
      ; push B[k][j] into the stack
      push dword [es:eax]

      ; Push C[i][j] into the stack (8 bytes)
      push dword [fs:edi]
      push dword [fs:edi+4]

      ; call the multiply routine
      call multiply

      ; pop the calculated value back to C[i][j]
      pop dword [fs:edi+4]      
      pop dword [fs:edi]
      ; clean the stack
      pop eax
      pop eax
      
      ; increment 'k' and continue loop
      inc ecx
      jmp L3

    end3:                ; End of loop3

    ; make edx point to the next element in C
    add edi, 8

    ; increment 'j' and continue loop
    inc ebx
    jmp L2

  end2:                  ; End of loop2

  ; increment 'i' and continue loop
  inc esi
  jmp L1

end1:                    ; End of loop1

